let parsed = obras.split("\n");
let scanIndex = 1;
let n = parseInt(nextLine());

let titles = [];
let authors = [];
let eras = [];

let poems = [];

let currentTitle = "";
let currentAuthor = "";
let currentEra = "";

for (let i = 0; i < n; i++) {
    titles.push(nextLine());
    authors.push(nextLine());
    eras.push(nextLine());

    let poem = [];
    while(currentLine() != "[end]") {
        poem.push(nextLine());
    }
    nextLine();

    poems.push(poem);
}

function nextLine() {
    scanIndex++;
    return parsed[scanIndex - 1];
}

function currentLine() {
    return parsed[scanIndex];
}

function generateSnippet() {
    document.getElementById("title").innerHTML = "Obra: ";
    document.getElementById("author").innerHTML = "Autor: ";
    document.getElementById("era").innerHTML = "Epoca: ";
    
    let snippetSize = parseInt(document.getElementById("input").value);

    if (isNaN(snippetSize)) {
        snippetSize = 10;
    } else if (snippetSize < 2) {
        snippetSize = 2;
    }

    let poemIndex = Math.floor(Math.random() * poems.length);
    let poem = poems[poemIndex]

    if (snippetSize > poem.length) {
        snippetSize = poem.length
    }

    let start = Math.floor(Math.random() * (poem.length - snippetSize));

    let snippet = "";
    for (let i = start; i < start + snippetSize; i++) {
        snippet += poem[i] + "<br />";
    }

    currentTitle = titles[poemIndex];
    currentAuthor = authors[poemIndex];
    currentEra = eras[poemIndex];

    document.getElementById("poem").innerHTML = snippet;
}

function showAnswers() {
    document.getElementById("title").innerHTML = "Obra: " + currentTitle;
    document.getElementById("author").innerHTML = "Autor: " + currentAuthor;
    document.getElementById("era").innerHTML = "Epoca: " + currentEra;
}
